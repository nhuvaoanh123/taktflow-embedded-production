uint8_t E2eProtection::crc8(uint8_t const* data, uint8_t length, uint8_t dataId)
{
    uint8_t crc = 0xFFu;
    crc = crc8Table[crc ^ dataId];
    for (uint8_t i = 0u; i < length; ++i)
    {
        crc = crc8Table[crc ^ data[i]];
    }
    return crc ^ 0xFFu;
}

void E2eProtection::protect(uint8_t* frameData, uint8_t dlc, uint8_t dataId, uint8_t& aliveCounter)
{
    frameData[0] = (frameData[0] & 0xF0u) | (dataId & 0x0Fu);
    frameData[0] = (frameData[0] & 0x0Fu) | ((aliveCounter & 0x0Fu) << 4u);

    uint8_t crc = crc8(frameData, dlc, dataId);
    frameData[1] = crc;

    aliveCounter = (aliveCounter + 1u) & 0x0Fu;
}

bool E2eProtection::validate(uint8_t const* frameData, uint8_t dlc, uint8_t dataId)
{
    uint8_t crc = crc8(frameData, dlc, dataId);
    return (crc == frameData[1]);
}

